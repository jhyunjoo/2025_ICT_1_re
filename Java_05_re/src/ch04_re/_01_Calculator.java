package ch04_re;

public class _01_Calculator {

	// add
	public double add(double n1, double n2) {
		return n1 + n2;
	}
	// sub
	public double sub(double n1, double n2) {
		return n1 - n2;
	}
	// mul
	public double mul(double n1, double n2) {
		return n1 * n2;
	}
	// div
	public double div(double n1, double n2) {
		return n1 / n2;
	}
}
